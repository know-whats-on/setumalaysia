function buildTasks(baseDate: Date, now: Date): ExecutionTask[] {
  const createdAt = isoNow(now);
  let undatedCounter = 0;
  const sourcedInputs = seedTaskInputs.filter((input) =>
    isApprovedSeedSourceDocumentName(input.source_document),
  );
  const sourceTaskIds = new Set(sourcedInputs.map((input) => input.id));
  const tasks = sourcedInputs.map((input, index): ExecutionTask => {
    const assignedDay = input.day || 15 + undatedCounter;
    if (!input.day) undatedCounter += 1;
    const assignedDate = dateFromDay(baseDate, assignedDay);
    return {
      id: input.id,
      title: input.title,
      description: input.description,
      source_document: input.source_document,
      source_section: input.source_section,
      source_excerpt: input.source_excerpt,
      workstream: input.workstream,
      category: input.category,
      project: input.project,
      priority: input.priority,
      impact: input.impact,
      effort_minutes: input.effort_minutes,
      urgency: input.urgency,
      status: input.status || 'Not started',
      scheduled_date: assignedDate,
      due_date: assignedDate,
      dependencies: (input.dependencies || []).filter((dependencyId) => sourceTaskIds.has(dependencyId)),
      prerequisite_of: [],
      sequence_order: index + 1,
      owner: FOUNDER_OWNER,
      related_partner: input.related_partner,
      related_campaign: input.related_campaign,
      related_event: input.related_event,
      related_platform: input.related_platform,
      related_city: input.related_city,
      notes: input.notes,
      evidence_required: input.evidence_required,
      completion_proof: '',
      ai_generated: true,
      manually_added: false,
      created_at: createdAt,
      updated_at: createdAt,
    };
  });

  const byId = new Map(tasks.map((task) => [task.id, task]));
  for (const task of tasks) {
    for (const dependencyId of task.dependencies) {
      const dependency = byId.get(dependencyId);
      if (dependency && !dependency.prerequisite_of.includes(task.id)) {
        dependency.prerequisite_of.push(task.id);
      }
    }
  }

  return tasks;
}

