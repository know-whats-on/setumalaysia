export function createInitialExecutionState(now = new Date()): ExecutionState {
  const createdAt = isoNow(now);
  const tasks = buildTasks(now, now);
  const today = toISODate(now);
  const todayTaskIds = tasks
    .filter((task) => task.scheduled_date === today)
    .map((task) => task.id);

  return {
    version: EXECUTION_STORAGE_VERSION,
    source_documents: sourceDocuments(now),
    tasks,
    candidate_tasks: createCandidateTasks(now, now),
    daily_plans: [
      {
        id: `plan-${today}`,
        date: today,
        task_ids: todayTaskIds,
        target_minutes: 120,
        locked_task_ids: [],
        generated_at: createdAt,
        planner_version: 'seed-v2',
      },
    ],
    analytics_events: [],
    settings: {
      target_daily_minutes: 120,
      min_daily_minutes: 90,
      max_daily_minutes: 135,
      founder_name: 'Founder',
    },
    created_at: createdAt,
    updated_at: createdAt,
  };
}
